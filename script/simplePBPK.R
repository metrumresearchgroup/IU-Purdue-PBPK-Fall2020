## This R script compiles and runs simulations for simplePBPK model

## load libraries
library(tidyverse)
library(mrgsolve)

#############################################################################################
##############################  Chunk 1: Compile model  #####################################
#############################################################################################

mod <- mread("simplePBPK", "models")

#############################################################################################
#############################################################################################


#############################################################################################
#########################  Chunk 2: Run a simple simulation  ################################
#############################################################################################

## Simulate a 100 mg dose given as an IV bolus
mod %>%
  ev(amt=100, cmt="VEN") %>%
  mrgsim(end=100) %>%
  plot()

